function openNav() {
    document.getElementById("cont").style.width = "25%";
    document.getElementById("main").style.width = "43%";
    document.getElementById("main").style.left = "40%";
}
    
function closeNav() {
    document.getElementById("cont").style.width = "0";
    document.getElementById("main").style.width = "50%";
    document.getElementById("main").style.left = "25%";
}